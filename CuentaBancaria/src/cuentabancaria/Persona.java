package cuentabancaria;

public class Persona {
    
    private String nombre;
    private String apellidos;
    private String nif;
    private int edad;

    public Persona(String nombre, String apellidos, String nif, int edad) {
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.nif = nif;
        this.edad = edad;
    }
    
}
